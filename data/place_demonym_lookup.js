  _indices: null,

  /**
   * Build reverse lookup indices for fast queries
   * Called once on page load
   */
  buildIndices: function () {
    if (this._indices) return; // Already built

    this._indices = {
      // variant (lowercase) → canonical key
      variantToCanonical: {},

      // demonym (lowercase) → canonical key
      demonymToCanonical: {},

      // type → array of canonical keys
      typeToCanonical: {
        country: [],
        country: [],
        region: [],
        city: []
      }
    };

    // Build indices
    Object.keys(this.places).forEach(canonicalKey => {
      const place = this.places[canonicalKey];

      // Index all variants
      place.variants.forEach(variant => {
        this._indices.variantToCanonical[variant.toLowerCase()] = canonicalKey;
      });

      // Index all demonyms
      place.demonyms.forEach(demonym => {
        this._indices.demonymToCanonical[demonym.toLowerCase()] = canonicalKey;
      });

      // Index by type
      this._indices.typeToCanonical[place.type].push(canonicalKey);
    });
  },

  /**
   * Find place by any variant name
   * @param {string} variant - Any place name variant (e.g., "USA", "America")
   * @returns {Object|null} - Place object or null
   */
  findPlaceByVariant: function (variant) {
    if (!this._indices) this.buildIndices();

    const canonicalKey = this._indices.variantToCanonical[variant.toLowerCase()];
    if (!canonicalKey) return null;

    return {
      canonical: this.places[canonicalKey].canonical,
      variants: this.places[canonicalKey].variants,
      demonyms: this.places[canonicalKey].demonyms,
      type: this.places[canonicalKey].type,
      metadata: this.places[canonicalKey].metadata
    };
  },

  /**
   * Find place by demonym
   * @param {string} demonym - Demonym (e.g., "American", "British")
   * @returns {Object|null} - Place object or null
   */
  findPlaceByDemonym: function (demonym) {
    if (!this._indices) this.buildIndices();

    const canonicalKey = this._indices.demonymToCanonical[demonym.toLowerCase()];
    if (!canonicalKey) return null;

    return {
      canonical: this.places[canonicalKey].canonical,
      variants: this.places[canonicalKey].variants,
      demonyms: this.places[canonicalKey].demonyms,
      type: this.places[canonicalKey].type,
      metadata: this.places[canonicalKey].metadata
    };
  },

  /**
   * Get all places of a specific type
   * @param {string} type - Place type (country | country | region | city)
   * @returns {Array} - Array of place objects
   */
  getPlacesByType: function (type) {
    if (!this._indices) this.buildIndices();

    const canonicalKeys = this._indices.typeToCanonical[type] || [];
    return canonicalKeys.map(key => ({
      canonical: this.places[key].canonical,
      variants: this.places[key].variants,
      demonyms: this.places[key].demonyms,
      type: this.places[key].type,
      metadata: this.places[key].metadata
    }));
  },

  /**
   * Get all place variants (for backward compatibility with gazetteer)
   * @returns {Array} - Array of all place name variants
   */
  getAllVariants: function () {
    const variants = [];
    Object.values(this.places).forEach(place => {
      variants.push(...place.variants);
    });
    return variants;
  },

  /**
   * Legacy compatibility: Get variants by type
   */
  getVariantsByType: function (type) {
    const places = this.getPlacesByType(type);
    const variants = [];
    places.forEach(place => variants.push(...place.variants));
    return variants;
  },

  /**
   * Get all demonyms for a place including hierarchical parents
   * Traverses the partOf chain to collect demonyms from parent places
   * Example: Rome → Italian (from Italy) → European (from Europe)
   * Follows ALL branches in partOf array (e.g., Abkhazia → Georgia AND Russia)
   *
   * @param {string} placeVariant - Place name or variant (e.g., "Rome", "USA")
   * @returns {Array} - Array of {demonym, place, level, isDirect} objects
   */
  getAllDemonymsWithHierarchy: function(placeVariant) {
    if (!this._indices) this.buildIndices();

    // Find the canonical key for this place
    const baseCanonicalKey = this._indices.variantToCanonical[placeVariant.toLowerCase()];
    if (!baseCanonicalKey) return [];

    const allDemonyms = [];
    const visited = new Set();

    // Use BFS to traverse all branches
    const queue = [{key: baseCanonicalKey, level: 0}];

    while (queue.length > 0) {
        const {key: currentKey, level} = queue.shift();

        // Skip if already visited or max depth reached
        if (visited.has(currentKey) || level > 10) continue;
        visited.add(currentKey);

        const placeData = this.places[currentKey];
        if (!placeData) continue;

        // Add demonyms from current level
        if (placeData.demonyms && placeData.demonyms.length > 0) {
            for (const demonym of placeData.demonyms) {
                // Avoid duplicates (same demonym from different paths)
                if (!allDemonyms.some(d => d.demonym.toLowerCase() === demonym.toLowerCase())) {
                    allDemonyms.push({
                        demonym: demonym,
                        place: placeData.canonical,
                        level: level,
                        isDirect: level === 0
                    });
                }
            }
        }

        // Add ALL parents to queue (follows all branches)
        const partOf = placeData.partOf;
        if (partOf && partOf.length > 0) {
            for (const parentKey of partOf) {
                if (!visited.has(parentKey)) {
                    queue.push({key: parentKey, level: level + 1});
                }
            }
        }
    }

    return allDemonyms;
  }
